Three decades ago when intellectual disability was still ‘mental illness’ or “mental retardation”, our daily ‘Chaanan Association’ had faded in our quest for ‘Normalcy’. Decades of interaction with our work associates has taught us that it is not ‘disability’ it is ‘diversity’ – a lesson that brings people together.

